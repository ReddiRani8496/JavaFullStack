    private void SearchBtnInUpdateOffenceActionPerformed(java.awt.event.ActionEvent evt) {
        int offenceId = Integer.parseInt(jTextField6.getText());
        RTOOperations rto = new RTOOperationsImpl();
        OffenceEO offence = rto.fetchOffencesById(offenceId);
        OffenceIdInUpdateOffence.setText(String.valueOf(offence.getOffenceId()));
        OffenceTypeFieldInUpdateOffence.setText(offence.getOffenceType());
        PenaltyFieldInUpdateOffence.setText(String.valueOf(offence.getPenalty()));
        VehicleTypeFieldInUpdateOffence.setText(offence.getVehType());
    }
    
